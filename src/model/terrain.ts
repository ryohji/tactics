// 地形SDFツールキット（W2 / it-5 でツールキット化）。真の3Dボリューム地形を符号付き距離場で表す。
// 分類（W3: sdf(center) ≤ d）と描画（W6: Marching Cubes 等値面）の唯一のソース。
// 具体地形（廃墟大聖堂）は cathedral.ts がこの toolkit を組み合わせて構築する（ブループリント層）。
//
// 評価は仕様の「格子座標空間」で行う（worldPos 前）。worldPos は正規直交基底
// e1,e2,e3 × スカラ S なので、格子座標のユークリッド距離はワールド距離(S=1)に一致する。
// → 格子座標で組んだプリミティブSDFは、そのまま格子単位の正しい符号付き距離になる。
//
// 形状は (a, u, b) フレームで定義する。これは worldPos の各軸（S=1）そのもの:
//   a = (x - y)/√2        水平 X
//   u = (x + y + z)/√3    鉛直（上, [111]方向）
//   b = (x + y - 2z)/√6   水平 Z
// (a,u,b) は格子座標の正規直交回転なので、ここで作った距離場は格子座標でも正しい距離。
// Three 非依存。

export type Vec3 = readonly [number, number, number];

export interface Terrain {
  /** 負=地形内部, 正=外部, 値=最近接表面までの符号付き距離（格子座標単位）。p は格子座標。 */
  sdf(p: Vec3): number;
  /** 等値面 sdf=0 を Marching Cubes で三角形化。positions は格子座標。res=各軸のセル分割数。 */
  mesh(res: number): TerrainMesh;
}

export interface TerrainMesh {
  /** 頂点座標 [x0,y0,z0, x1,y1,z1, ...]（格子座標）。法線は W6 側で計算してよい。 */
  positions: Float32Array;
  /** 三角形インデックス（positions の頂点番号、3つで1三角形）。 */
  indices: Uint32Array;
}

const SQRT2 = Math.SQRT2;
const SQRT3 = Math.sqrt(3);
const SQRT6 = Math.sqrt(6);

/** 格子座標 → (a,u,b) フレーム（worldPos の各軸, S=1）。正規直交。 */
export function toFrame(p: Vec3): [number, number, number] {
  const [x, y, z] = p;
  return [(x - y) / SQRT2, (x + y + z) / SQRT3, (x + y - 2 * z) / SQRT6];
}

/** (a,u,b) フレーム → 格子座標（toFrame の逆, p = a·e1 + u·e3 + b·e2）。 */
export function fromFrame(a: number, u: number, b: number): Vec3 {
  return [
    a / SQRT2 + u / SQRT3 + b / SQRT6,
    -a / SQRT2 + u / SQRT3 + b / SQRT6,
    u / SQRT3 - (2 * b) / SQRT6,
  ];
}

// --- プリミティブSDF（フレーム座標 [a,u,b] で評価。すべて正確な符号付き距離）---

/** 中心 c, 半径 r の球。 */
export function sdSphere(fr: readonly [number, number, number], c: Vec3, r: number): number {
  const dx = fr[0] - c[0];
  const dy = fr[1] - c[1];
  const dz = fr[2] - c[2];
  return Math.hypot(dx, dy, dz) - r;
}

/** 中心 c, 半径(半長) he の軸平行ボックス（フレーム軸に平行＝正規直交なので距離は正確）。 */
export function sdBox(fr: readonly [number, number, number], c: Vec3, he: Vec3): number {
  const qx = Math.abs(fr[0] - c[0]) - he[0];
  const qy = Math.abs(fr[1] - c[1]) - he[1];
  const qz = Math.abs(fr[2] - c[2]) - he[2];
  const outside = Math.hypot(Math.max(qx, 0), Math.max(qy, 0), Math.max(qz, 0));
  const inside = Math.min(Math.max(qx, Math.max(qy, qz)), 0);
  return outside + inside;
}

/**
 * 中心 c, 軸 axis(単位ベクトル), 半長 h, 半径 r の有限円柱（両端キャップ）。
 * axis は任意方向でよい（倒れた柱）。フレームは正規直交なので、軸方向成分と
 * 半径方向成分に直交分解した量から組む符号付き距離は正確になる。
 */
export function sdCappedCylinder(
  fr: readonly [number, number, number],
  c: Vec3,
  axis: Vec3,
  h: number,
  r: number,
): number {
  const vx = fr[0] - c[0];
  const vy = fr[1] - c[1];
  const vz = fr[2] - c[2];
  const ax = vx * axis[0] + vy * axis[1] + vz * axis[2]; // 軸方向成分
  // 半径方向ベクトル = v - ax·axis。
  const rx = vx - ax * axis[0];
  const ry = vy - ax * axis[1];
  const rz = vz - ax * axis[2];
  const dr = Math.hypot(rx, ry, rz) - r; // 側面までの距離
  const da = Math.abs(ax) - h; // キャップまでの距離
  const outside = Math.hypot(Math.max(dr, 0), Math.max(da, 0));
  const inside = Math.min(Math.max(dr, da), 0);
  return outside + inside;
}

/** 3本の正規直交軸 basis=[ex,ey,ez] で回転した、中心 c・半長 he のボックス（傾いたスラブ）。 */
export function sdBoxOriented(
  fr: readonly [number, number, number],
  c: Vec3,
  he: Vec3,
  basis: readonly [Vec3, Vec3, Vec3],
): number {
  const vx = fr[0] - c[0];
  const vy = fr[1] - c[1];
  const vz = fr[2] - c[2];
  // ローカル座標へ射影（basis は正規直交なので長さ保存＝距離が正確）。
  const lx = vx * basis[0][0] + vy * basis[0][1] + vz * basis[0][2];
  const ly = vx * basis[1][0] + vy * basis[1][1] + vz * basis[1][2];
  const lz = vx * basis[2][0] + vy * basis[2][1] + vz * basis[2][2];
  const qx = Math.abs(lx) - he[0];
  const qy = Math.abs(ly) - he[1];
  const qz = Math.abs(lz) - he[2];
  const outside = Math.hypot(Math.max(qx, 0), Math.max(qy, 0), Math.max(qz, 0));
  const inside = Math.min(Math.max(qx, Math.max(qy, qz)), 0);
  return outside + inside;
}

/** 滑らかな union（k>0 で接合をブレンド）。k=0 なら厳密な min。瓦礫を床へなじませる。 */
export function smoothUnion(a: number, b: number, k: number): number {
  if (k <= 0) return Math.min(a, b);
  const h = Math.max(k - Math.abs(a - b), 0) / k;
  return Math.min(a, b) - h * h * k * 0.25;
}

/** subtract（A から B をくり抜く）。負=内部の規約で max(A, -B)。 */
export function subtract(a: number, b: number): number {
  return Math.max(a, -b);
}

export const DEG = Math.PI / 180;

/** Rodrigues 回転：単位軸 axis まわりに v を ang ラジアン回す。 */
export function rotateAxisAngle(v: Vec3, axis: Vec3, ang: number): Vec3 {
  const c = Math.cos(ang);
  const s = Math.sin(ang);
  const d = v[0] * axis[0] + v[1] * axis[1] + v[2] * axis[2];
  const cx = axis[1] * v[2] - axis[2] * v[1];
  const cy = axis[2] * v[0] - axis[0] * v[2];
  const cz = axis[0] * v[1] - axis[1] * v[0];
  return [
    v[0] * c + cx * s + axis[0] * d * (1 - c),
    v[1] * c + cy * s + axis[1] * d * (1 - c),
    v[2] * c + cz * s + axis[2] * d * (1 - c),
  ];
}

/** 標準基底 (a,u,b 軸) を axis まわりに ang 回した正規直交基底。傾いたスラブの向きに使う。 */
export function tiltedBasis(axis: Vec3, ang: number): [Vec3, Vec3, Vec3] {
  return [
    rotateAxisAngle([1, 0, 0], axis, ang),
    rotateAxisAngle([0, 1, 0], axis, ang),
    rotateAxisAngle([0, 0, 1], axis, ang),
  ];
}

